    public Quest3() {
        initComponents();
        TimerTask task = new TimerTask(){
            public void run(){
                try{
                    System.out.println("running");
                    i--;
                    timer_level.setText(i+"");
                    if(i==0){
                        timer.cancel();
                        System.out.println("stop");
                        GameOver o = new GameOver();
                        o.setVisible(true);
                        close();
                    }
                }
                catch(Exception e){
                    System.out.print(e);
                }  
            }
        };
        timer.scheduleAtFixedRate(task, 0, 1000);
    }

